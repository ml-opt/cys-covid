#ifndef DNN_OPT_CORE_GENERATOR
#define DNN_OPT_CORE_GENERATOR

#include "stdexcept"

namespace dnn_opt
{
namespace core
{

/**
 * @brief The generator class is intended as an interface for
 * custom generators. A generator generates
 * random numbers to be used as parameters to optimize.
 *
 * @author Jairo Rojas-Delgado <jrdelgado@uci.cu>
 * @date June, 2016
 */
class generator
{
public:

  static const generator* get_numpy();

  /**
   * @brief Generate a sequence of random numbers.
   *
   * @param count the amount of random numbers to generate.
   *
   * @param[out] parameters an array of size @count where to store the
   * generated random numbers.
   */
  virtual void generate(int count, float* params) const = 0;

  /**
   * @brief Generate a single random number.
   *
   * @return the generated random number.
   */
  virtual float generate() const = 0;

  virtual void set_constraints(int count, float* params) const = 0;

  /**
   * @brief The minimun value that this generator produces.
   *
   * @return the minimun value.
   */
  float get_min() const;

  /**
   * @brief The maximun value that this generator produces.
   *
   * @return the maximun value.
   */
  float get_max() const;

  /**
   * @brief Change the value of the minimun number generated by this generator.
   *
   * @param min the new minimun value.
   */
  void set_min(float min);

  /**
   * @brief Change the value of the maximun number generated by this generator.
   *
   * @param min the new maximun value.
   */
  void set_max(float max);

  float get_ext() const;

  /**
   * The basic destructor of this class.
   */
  virtual ~generator();

protected:

  /**
   * @brief The basic constructor for this class.
   *
   * @param min the minimun value this generator produces.
   *
   * @param max the maximun value this generator produces.
   *
   * @throws std::range_error if min is not less than max.
   */
  generator(float min, float max);

  /** The minimun value this generator produces */
  float m_min;

  /** The maximun value this generator produces */
  float m_max;

  class numpy;

  static numpy* m_numpy;

};

/**
 * @brief The generator::numpy class is a generator that is
 * intended to be used when you expect to specify the generator latter.
 *
 * This numpy generator function will throw an @ref std::logic_error when
 * someone try to use it for actual generation.
 *
 * @author Jairo Rojas-Delgado <jrdelgado@uci.cu>
 * @date March, 2020
 */
class generator::numpy : public generator
{
public:

  inline static generator::numpy* make()
  {
    return new numpy();
  }

  /**
   * @copydoc generator::generate()
   * @throws @throws std::logic_error always.
   */
  inline virtual void generate(int count, float* params) const override
  {
    throw std::logic_error("You are currently using a numpy generator");
  }

  /**
   * @copydoc generator::generate()
   * @throws @throws std::logic_error always.
   */
  inline virtual float generate() const override
  {
    throw std::logic_error("You are currently using a numpy generator");
  }

  /**
   * @copydoc generator::set_constraints()
   * @throws @throws std::logic_error always.
   */
  inline virtual void set_constraints(int count, float* params) const override
  {
    throw std::logic_error("You are currently using a numpy generator");
  }

protected:

  inline numpy() : generator(0.0f, 0.0f)
  {

  }
};

} // namespace core
} // namespace dnn_opt

#endif
